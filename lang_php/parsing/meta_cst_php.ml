  | Default ((v1, v2, v3)) ->
      let v1 = vof_tok v1
      and v2 = vof_tok v2
      and v3 = vof_list vof_stmt_and_def v3
      in OCaml.VSum (("Default", [ v1; v2; v3 ]))
