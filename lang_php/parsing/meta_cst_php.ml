      and v2 = vof_tok v2
      and v3 = vof_foreach_pattern v3
      in OCaml.VSum (("ForeachArrow", [ v1; v2; v3 ]))
  | ForeachList ((v1, v2)) ->
      let v1 = vof_tok v1
      and v2 = vof_paren (vof_comma_list vof_list_assign) v2
      in OCaml.VSum (("ForeachList", [ v1; v2 ]))
